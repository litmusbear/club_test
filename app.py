# UI 설정
st.set_page_config(page_title="나에게 가장 잘 맞는 동아리는?", layout="centered")
st.title("🧪 동아리 성향 테스트")
st.info("각 질문에 정답은 없습니다. 생각나는 대로 자유롭게 적어주세요!")

# 질문 리스트 정의
questions = [
    "1. 평소의 취미가 무엇인가요?",
    "2. 잘하는 것이 있다면 무엇인가요?",
    "3. 좋아하는 것이 무엇인가요?",
    "4. 평소에 해보고 싶었던게 무엇인가요?",
    "5. 다른 사람들과 다르게 공감하지 못하는것은 무엇인가요?",
    "6. 존경하는 '것'은 무엇인가요?",
    "7. 싫어하는 것은 무엇인가요?",
    "8. 다른 사람들에게는 없는 자신만의 장점은 무엇인가요?",
    "9. 자신의 단점은 무엇인가요?",
    "10. 자신만의 자랑거리는 무엇인가요?"
]

# 입력창 생성 (반복문으로 효율화)
all_answers = []
for i, q in enumerate(questions):
    ans = st.text_area(q, key=f"q{i}", height=100)
    all_answers.append(ans)

# 분석 로직
if st.button("내 성향 분석하기", use_container_width=True):
    # 결과 초기화
    results = {name: 0 for name in club_data.keys()}
    match_details = {name: [] for name in club_data.keys()}

    # 각 답변별로 해당하는 키워드 뭉치와 비교
    for i, user_ans in enumerate(all_answers):
        if not user_ans.strip():  # 답변이 비어있으면 건너뜀
            continue

        for club_name, keyword_lists in club_data.items():
            # i번째 질문에 대응하는 키워드 리스트 가져오기
            current_keywords = keyword_lists[i]

            for word in current_keywords:
                if word in user_ans:
                    count = user_ans.count(word)
                    results[club_name] += count
                    match_details[club_name].append(f"{word}({i + 1}번)")

    # 결과 산출
    max_score = max(results.values())

    if max_score == 0:
        st.error("입력하신 문장에서 매칭되는 키워드를 찾지 못했어요. 조금 더 자세히 적어볼까요?")
    else:
        winners = [name for name, score in results.items() if score == max_score]
        st.balloons()
        st.success(f"분석 완료! 당신에게 가장 어울리는 동아리는 **{', '.join(winners)}** 입니다.")

        # 상세 분석 결과 시각화
        st.divider()
        st.subheader("📊 동아리별 적합도 지수")
        st.bar_chart(results)

        # 어떤 단어 때문에 점수가 나왔는지 보여주기
        with st.expander("📝 키워드 매칭 상세 내역 보기"):
            for club in results:
                if results[club] > 0:
                    st.write(f"**{club}**: {', '.join(match_details[club])} -> 총 {results[club]}점")
